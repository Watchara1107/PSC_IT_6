<?php 
    /*
        โครงสร้างของ Loop do...whlie
        ใช้เมื่อมีการแสดงรอบก่อนอย่างน้อย 1 รอบ
        do{
            แสดงผลลัพธ์จำนวนรอบ;
            เพิ่มค่า-ลดค่า;
        }whlie(เงื่อนไขจำนวนรอบ)
    */

    //โจทย์ ให้ทำวนลูปจำนวน 10 รอบ ให้แสดงรอบที่ 1 ก่อน 1 รอบ
    $start = 1;
    do{
        echo "รอบที่".$start."<br>";
        $start++;

    }while($start<=10)
?>