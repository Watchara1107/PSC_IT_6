<?php 
    /*
        โครงสร้างของ Loop While
        ใช้ในกรณีที่ไม่รู้จำนวนรอบ
        while(เงื่อนไขจำนวนลูป){
            แสดงผลลัพธ์ของ Loop;
            เพิ่มค่า-ลดค่า;
        }
    */

    //โจทย์ ให้ทำวนลูปจำนวน 10 รอบ 
    $start =10;  //ค่าเริ่มต้น
    $stop = 1;  //ค่าสิ้นสุด
    while($start >= $stop){
        //++$start;
        echo "รอบที่".$start."<br>";
        $start--;
       
    }

?>